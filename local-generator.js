const ffmpegPath = require('ffmpeg-static');
const { execFile } = require('child_process');
const path = require('path');
const fs = require('fs');
const { v4: uuidv4 } = require('uuid');
const Jimp = require('jimp');

const VIDEOS_DIR = path.join(__dirname, 'videos');
const W = 1280, H = 720;

function hexToRgb(hex) {
  const r = parseInt(hex.slice(1, 3), 16);
  const g = parseInt(hex.slice(3, 5), 16);
  const b = parseInt(hex.slice(5, 7), 16);
  return [r, g, b];
}

async function generateVideo(prompt, duration = 5) {
  const jobId = uuidv4();
  const outputPath = path.join(VIDEOS_DIR, `${jobId}.mp4`);

  if (!fs.existsSync(VIDEOS_DIR)) {
    fs.mkdirSync(VIDEOS_DIR, { recursive: true });
  }

  const fps = 10;
  const totalFrames = duration * fps;
  const textH = 500;
  const scrollH = H + textH;

  const scrollImage = new Jimp(W, scrollH);
  const topBg = hexToRgb('#0d0d1a');
  const midBg = hexToRgb('#1a1a3e');
  const botBg = hexToRgb('#2d1b4e');

  for (let y = 0; y < scrollH; y++) {
    const t = y / scrollH;
    const r = Math.floor(topBg[0] + (midBg[0] - topBg[0]) * Math.min(1, t * 2));
    const g = Math.floor(topBg[1] + (midBg[1] - topBg[1]) * Math.min(1, t * 2));
    const b = Math.floor(topBg[2] + (botBg[2] - topBg[2]) * t);
    for (let x = 0; x < W; x++) {
      const nx = x / W, ny = y / scrollH;
      const rr = Math.max(0, Math.min(255, r + Math.floor(Math.sin(nx * 6 + ny * 3) * 20)));
      const gg = Math.max(0, Math.min(255, g + Math.floor(Math.cos(nx * 8 + ny * 4) * 20)));
      const bb = Math.max(0, Math.min(255, b + Math.floor(Math.sin(nx * 5 - ny * 3) * 20)));
      scrollImage.setPixelColor(Jimp.rgbaToInt(rr, gg, bb, 255), x, y);
    }
  }

  const titleFont = await Jimp.loadFont(Jimp.FONT_SANS_64_WHITE);
  const subFont = await Jimp.loadFont(Jimp.FONT_SANS_32_WHITE);
  const promptFont = await Jimp.loadFont(Jimp.FONT_SANS_32_WHITE);

  const titleText = 'VideoForge AI';
  const subText = 'Generated by AI';
  const promptLines = [];
  let line = '';
  for (const word of prompt.split(' ')) {
    if ((line + ' ' + word).length > 32) {
      promptLines.push(line);
      line = word;
    } else {
      line = (line ? line + ' ' : '') + word;
    }
  }
  if (line) promptLines.push(line);

  const titleW = Jimp.measureText(titleFont, titleText);
  scrollImage.print(titleFont, (W - titleW) / 2, 40, titleText);

  const subW = Jimp.measureText(subFont, subText);
  scrollImage.print(subFont, (W - subW) / 2, 110, subText);

  let py = H - 40;
  for (const pline of promptLines) {
    const pw = Jimp.measureText(promptFont, pline);
    scrollImage.print(promptFont, (W - pw) / 2, py, pline);
    py += 45;
  }

  const tempDir = path.join(VIDEOS_DIR, 'tmp_' + jobId);
  fs.mkdirSync(tempDir, { recursive: true });

  const maxY = scrollH - H;
  for (let f = 0; f < totalFrames; f++) {
    const yPos = Math.round((f / (totalFrames - 1)) * maxY);
    const frame = scrollImage.clone().crop(0, yPos, W, H);
    const idx = String(f).padStart(4, '0');
    await frame.writeAsync(path.join(tempDir, `frame_${idx}.png`));
  }

  const args = [
    '-y',
    '-framerate', String(fps),
    '-i', path.join(tempDir, 'frame_%04d.png'),
    '-c:v', 'libx264',
    '-pix_fmt', 'yuv420p',
    '-preset', 'fast',
    '-crf', '28',
    outputPath
  ];

  return new Promise((resolve, reject) => {
    const proc = execFile(ffmpegPath, args, async (err) => {
      try { fs.rmSync(tempDir, { recursive: true, force: true }); } catch (e) {}
      if (err) {
        reject(new Error(err.message));
      } else {
        resolve({
          jobId,
          videoUrl: `/videos/${jobId}.mp4`,
          outputPath
        });
      }
    });
    proc.stderr.on('data', () => {});
  });
}

module.exports = { generateVideo };
