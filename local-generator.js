const ffmpegPath = require('ffmpeg-static');
const { execFile } = require('child_process');
const path = require('path');
const fs = require('fs');
const { v4: uuidv4 } = require('uuid');

const VIDEOS_DIR = path.join(__dirname, 'videos');
const FONT_PATH = path.join(
  process.env.SystemRoot || 'C:\\Windows',
  'Fonts', 'arial.ttf'
);

function escapeDrawText(text) {
  return text
    .replace(/\\/g, '\\\\')
    .replace(/:/g, '\\:')
    .replace(/%/g, '\\%')
    .replace(/{/g, '\\{')
    .replace(/}/g, '\\}')
    .replace(/'/g, "\\'");
}

function generateVideo(prompt, duration = 5) {
  return new Promise((resolve, reject) => {
    const jobId = uuidv4();
    const outputPath = path.join(VIDEOS_DIR, `${jobId}.mp4`);

    if (!fs.existsSync(VIDEOS_DIR)) {
      fs.mkdirSync(VIDEOS_DIR, { recursive: true });
    }

    const escapedPrompt = escapeDrawText(prompt);
    const scrollSpeed = (720 + 200) / duration;

    const filters = [
      `drawtext=text='VideoForge AI':fontfile=${FONT_PATH}:fontsize=48:fontcolor=#6c63ff:x=(w-text_w)/2:y=40:enable='between(t,0,${duration})'`,
      `drawtext=text='Generated by AI':fontfile=${FONT_PATH}:fontsize=20:fontcolor=#6868a8:x=(w-text_w)/2:y=95:enable='between(t,0,${duration})'`,
      `drawtext=text='${escapedPrompt}':fontfile=${FONT_PATH}:fontsize=36:fontcolor=white:x=(w-text_w)/2:y=h-100-t*${scrollSpeed}:enable='between(t,0,${duration})'`
    ];

    const args = [
      '-y',
      '-f', 'lavfi', '-i', `color=c=#0a0a12:s=1280x720:d=${duration}`,
      '-vf', filters.join(','),
      '-c:v', 'libx264',
      '-pix_fmt', 'yuv420p',
      '-preset', 'fast',
      '-crf', '28',
      outputPath
    ];

    const proc = execFile(ffmpegPath, args, (err, stdout, stderr) => {
      if (err) {
        const errLines = (stderr || '').split('\n').filter(l => l.includes('Error'));
        reject(new Error(errLines.join('; ') || err.message));
      } else {
        resolve({
          jobId,
          videoUrl: `/videos/${jobId}.mp4`,
          outputPath
        });
      }
    });

    proc.stderr.on('data', () => {});
  });
}

module.exports = { generateVideo };
