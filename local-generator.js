const ffmpegPath = require('ffmpeg-static');
const { execFile } = require('child_process');
const path = require('path');
const fs = require('fs');
const { v4: uuidv4 } = require('uuid');

const VIDEOS_DIR = path.join(__dirname, 'videos');

function findFont() {
  const candidates = [
    path.join(__dirname, 'node_modules', 'dejavu-fonts-ttf', 'ttf', 'DejaVuSans.ttf'),
    path.join(__dirname, 'fonts', 'DejaVuSans.ttf'),
    path.join(process.env.SystemRoot || 'C:\\Windows', 'Fonts', 'arial.ttf'),
    '/usr/share/fonts/truetype/dejavu/DejaVuSans.ttf',
    '/usr/share/fonts/truetype/liberation/LiberationSans-Regular.ttf',
    '/usr/share/fonts/truetype/ubuntu/Ubuntu-Regular.ttf',
    '/usr/share/fonts/truetype/droid/DroidSans.ttf',
    '/usr/share/fonts/TTF/DejaVuSans.ttf',
    '/System/Library/Fonts/Helvetica.ttc',
  ];
  for (const c of candidates) {
    if (fs.existsSync(c)) return c;
  }
  return '';
}
const FONT_PATH = findFont();

function escapeDrawText(text) {
  return text
    .replace(/\\/g, '\\\\')
    .replace(/:/g, '\\:')
    .replace(/%/g, '\\%')
    .replace(/{/g, '\\{')
    .replace(/}/g, '\\}')
    .replace(/'/g, "\\'");
}

function generateVideo(prompt, duration = 5) {
  return new Promise((resolve, reject) => {
    const jobId = uuidv4();
    const outputPath = path.join(VIDEOS_DIR, `${jobId}.mp4`);

    if (!fs.existsSync(VIDEOS_DIR)) {
      fs.mkdirSync(VIDEOS_DIR, { recursive: true });
    }

    const escapedPrompt = escapeDrawText(prompt);
    const scrollSpeed = (720 + 100) / duration;
    const W = 1280, H = 720;

    let complexFilter;

    if (FONT_PATH) {
      const filterParts = [
        `color=c=#0d0d1a:s=${W}x${H}:d=${duration}[bg]`,
        `color=c=#1a1a3e:s=${W}x${H}:d=${duration}[c1]`,
        `color=c=#2d1b4e:s=${W}x${H}:d=${duration}[c2]`,
        `color=c=#0d0d2b:s=${W}x${H}:d=${duration}[c3]`,
        `[bg][c1]overlay=0:H-t*${H/duration}[g1]`,
        `[g1][c2]overlay=W/3:H-t*${H/duration}[g2]`,
        `[g2][c3]overlay=2*W/3:H-t*${H/duration}[base]`,
        `[base]drawtext=text='VideoForge AI':fontfile=${FONT_PATH}:fontsize=48:fontcolor=#7c73ff:x=(w-text_w)/2:y=40:enable='between(t,0,${duration})'[t1]`,
        `[t1]drawtext=text='Generated by AI':fontfile=${FONT_PATH}:fontsize=20:fontcolor=#8888bb:x=(w-text_w)/2:y=95:enable='between(t,0,${duration})'[t2]`,
        `[t2]drawtext=text='${escapedPrompt}':fontfile=${FONT_PATH}:fontsize=36:fontcolor=white:x=(w-text_w)/2:y=h-100-t*${scrollSpeed}:enable='between(t,0,${duration})'[final]`
      ];
      complexFilter = filterParts.join(';');
    } else {
      complexFilter = [
        `color=c=#0d0d1a:s=${W}x${H}:d=${duration}[bg]`,
        `color=c=#1a1a3e:s=${W}x${H}:d=${duration}[c1]`,
        `color=c=#2d1b4e:s=${W}x${H}:d=${duration}[c2]`,
        `color=c=#0d0d2b:s=${W}x${H}:d=${duration}[c3]`,
        `[bg][c1]overlay=0:H-t*${H/duration}[g1]`,
        `[g1][c2]overlay=W/3:H-t*${H/duration}[g2]`,
        `[g2][c3]overlay=2*W/3:H-t*${H/duration}[base]`
      ].join(';');
    }

    const mapLabel = FONT_PATH ? '[final]' : '[base]';
    const args = [
      '-y',
      '-filter_complex', complexFilter,
      '-map', mapLabel,
      '-c:v', 'libx264',
      '-pix_fmt', 'yuv420p',
      '-preset', 'fast',
      '-crf', '28',
      outputPath
    ];

    const proc = execFile(ffmpegPath, args, (err, stdout, stderr) => {
      if (err) {
        const errLines = (stderr || '').split('\n').filter(l => l.includes('Error'));
        reject(new Error(errLines.join('; ') || err.message));
      } else {
        resolve({
          jobId,
          videoUrl: `/videos/${jobId}.mp4`,
          outputPath
        });
      }
    });

    proc.stderr.on('data', () => {});
  });
}

module.exports = { generateVideo };
